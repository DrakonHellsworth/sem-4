//Print a pattern using System.out.println().
class exe3_3c
{
    public static void main(String args[])
    {
        System.out.println("   *   ");
        System.out.println("  ***  ");
        System.out.println(" ***** ");
        System.out.println("*******");
    }
}
//Observation: The program uses System.out.println() to print a pattern of asterisks (*) in the shape of a pyramid. Each line of the pattern is printed on a new line, creating a visual representation of the pyramid when the program is executed. The output is displayed to the user as specified in the print statements.