//Use System.out.printf() to print:Name:ravi,Marks (85.50)
class exe3_3d
{
    public static void main(String args[])
    {
        System.out.println("Name:Ravi");
        System.out.printf("Marks: %.2f", 85.50);
    }
}
//Observation: The program uses System.out.println() to print the name "Ravi" on a new line. It then uses System.out.printf() to format and print the marks (85.50) with two decimal places using the format specifier %.2f. The output is displayed to the user as specified in the print statements.