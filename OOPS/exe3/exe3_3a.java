//output using system.out,Use different print functions.
class exe3_3a
{
    public static void main(String args[])
    {
        System.out.print("This is using print function. ");
        System.out.println("This is using println function.");
        System.out.printf("This is using printf function. %d", 2024);
    }
}
//Observation: The program demonstrates the use of different print functions in Java. It uses System.out.print() to print a message without a newline, System.out.println() to print a message with a newline, and System.out.printf() to format and print a message with an integer value (2024) using a format specifier (%d). The output is displayed to the user as specified by each print function.