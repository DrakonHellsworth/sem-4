//Print a table of a number using formatted output.
class exe3_4
{
    public static void main(String args[])
    {
        int i,n=5;
        for(i=1;i<=10;i++)
        {
            System.out.println(n+"*"+i+"="+n*i);
        }
    }
}
//Observation: The program uses a for loop to iterate from 1 to 10, printing the multiplication table of the number 5. The output is formatted as "5*i=product" for each value of i, where product is the result of multiplying 5 by i. The output is displayed to the user as specified in the print statement within the loop.